    void mostrarCompras(int id) {
        ResultSet rs;
        int com = id;
        System.out.print("Numero de orden = '"+com+"'");
        String[] titulos={"Producto","Precio Unitario","Cantidad","SubTotal"};
        String[] registro=new String[4];
        tabla1=new DefaultTableModel(null, titulos);
        try {
          rs=ccompra.mostrarcompras(com);
        while(rs.next()){
            String produc=rs.getString("producto");
            registro[0]=produc;
            Productos prod = ccompra.mostrarPrecioUnitario(produc);
            double pres= prod.getPrecio();
            registro[1]=Double.toString(pres);
            registro[2]=rs.getString("cantidad");
            int canti=Integer.parseInt(rs.getString("cantidad"));
            if(canti==1){registro[3]=Double.toString(pres);}
            else{
            registro[3]=Double.toString(canti*pres);}
           tabla1.addRow(registro);
        } 
          jTable1.setModel(tabla1);
        } catch (SQLException ex) {
        Logger.getLogger(VerCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
